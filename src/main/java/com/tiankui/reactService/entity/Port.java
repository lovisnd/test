package com.tiankui.reactService.entity;

public class Port {
    private String id;//端口id
    private String flowName;//端口名
    private int physicalState;//物理状态
    private int operatingState;//操作状态
    private int realInflow;//实时流入流量
    private int realOutflow;//实时流出流量
    private int maxInflow;//流入峰值
    private int maxOutflow;//流出峰值

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFlowName() {
        return flowName;
    }

    public void setFlowName(String flowName) {
        this.flowName = flowName;
    }

    public int getPhysicalState() {
        return physicalState;
    }

    public void setPhysicalState(int physicalState) {
        this.physicalState = physicalState;
    }

    public int getOperatingState() {
        return operatingState;
    }

    public void setOperatingState(int operatingState) {
        this.operatingState = operatingState;
    }

    public int getRealInflow() {
        return realInflow;
    }

    public void setRealInflow(int realInflow) {
        this.realInflow = realInflow;
    }

    public int getRealOutflow() {
        return realOutflow;
    }

    public void setRealOutflow(int realOutflow) {
        this.realOutflow = realOutflow;
    }

    public int getMaxInflow() {
        return maxInflow;
    }

    public void setMaxInflow(int maxInflow) {
        this.maxInflow = maxInflow;
    }

    public int getMaxOutflow() {
        return maxOutflow;
    }

    public void setMaxOutflow(int maxOutflow) {
        this.maxOutflow = maxOutflow;
    }
}
